    public int getWorkingBits() {
        int sum = 0;
        for (int i = 0; i < constants.length; i++) {
            for (int j = 0; j < constants[0].length; j++) {
                if (constants[i][j] == 0) {
                    sum++;
                }
            }
        }
        return sum;
    }

