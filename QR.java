    final int PICTURE_LENGTH = 1450; // 1450x1450 image
    final int PIXEL_LENGTH = 29; // 29x29 pixel QR code
    BufferedImage image;
    Graphics2D canvas;

    public QR() {
        image = new BufferedImage(PICTURE_LENGTH, PICTURE_LENGTH, BufferedImage.TYPE_INT_RGB);
        canvas = image.createGraphics();
    }

    public void encode(String binaryData) {
        int dataIndex = 0;
        int size = PIXEL_LENGTH;
        boolean upwards = true;

        for (int col = size - 1; col > 0; col -= 2) {
            for (int row = (upwards ? size - 1 : 0); (upwards ? row >= 0 : row < size); row += (upwards ? -1 : 1)) {
                for (int offset = 0; offset < 2; offset++) {
                    int currentCol = col - offset;

                    if (constants[row][currentCol] == 0 && dataIndex < binaryData.length()) {
                        code[row][currentCol] = binaryData.charAt(dataIndex) == '1' ? 1 : 0;
                        dataIndex++;
                    }
                }
            }
            upwards = !upwards;
        }
    }

    public void printCode() {
        int xPos = 0, yPos = 0;
        for (int i = 0; i < PIXEL_LENGTH; i++) {
            for (int j = 0; j < PIXEL_LENGTH; j++) {
                canvas.setColor(code[i][j] == 0 ? Color.white : Color.black);
                canvas.fillRect(xPos, yPos, 50, 50);
                xPos += 50;
            }
            xPos = 0;
            yPos += 50;
        }

        try {
            File f = new File("QRCode.jpg");
            ImageIO.write(image, "jpg", f);
        } catch (IOException e) {
            System.out.println("Failure");
        }
    }

